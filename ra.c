#include "header.h"

void	ra(t_list **a)
{
	t_list	*tmp;

	tmp = *a;
	ft_lstadd_back(a, ft_lstnew(tmp->content));
	*a = tmp->next;
	free(tmp);
	write(1, "ra\n", 3);
}
