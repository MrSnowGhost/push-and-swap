#include "header.h"

void	rb(t_list **b)
{
	t_list	*tmp;

	tmp = *b;
	ft_lstadd_back(b, ft_lstnew(tmp->content));
	*b = tmp->next;
	free(tmp);
	write(1, "rb\n", 3);
}
